#include "libft.h"

void	ft_putnbr(int n)
{
	int x;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	x = n;
	if (n < 0)
	{
		ft_putchar('-');
		x = -n;
	}
	if (x >= 10)
	{
		ft_putnbr(x / 10);
		ft_putnbr(x % 10);
	}
	else
		ft_putchar(x + '0');
}
