#include "libft.h"

int	ft_atoi(const char *s)
{
	int	nbr;
	int	sign;

	nbr = 0;
	sign = 1;
	while (ft_isspace(*s))
		s++;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (ft_isdigit(*s))
	{
		nbr = nbr * 10 + *s - '0';
		s++;
	}
	return (nbr * sign);
}
